--------------------------------------------

local ns = vim.api.nvim_create_namespace("magic_cursor")

--------------------------------------------

Hydra({
    name = "Telescope",
    hint = hint_flower,
    config = {
        color = "pink",
        invoke_on_body = true,
        hint = {
            position = "bottom-right",
            border = "rounded",
        },
        on_enter = function()
            lib_highlighting.highlight_tag_indicators(ns)
        end,
        on_exit = function()
            vim.api.nvim_buf_clear_namespace(0, ns, 0, -1)
        end,
    },
    mode = "n",
    body = "<Leader><CR>",
    heads = {
        {
            "<C-j>",
            function()
                lib_magic_cursor.jump_to_previous_or_next_tag("next")
                vim.api.nvim_buf_clear_namespace(0, ns, 0, -1)
                lib_highlighting.highlight_tag_braces(ns)
            end,
            { nowait = true },
        },
        {
            "<C-k>",
            function()
                lib_magic_cursor.jump_to_previous_or_next_tag("previous")
                vim.api.nvim_buf_clear_namespace(0, ns, 0, -1)
                lib_highlighting.highlight_tag_braces(ns)
            end,
            { nowait = true },
        },
        { "<Esc>", nil, { exit = true, nowait = true } },
        { "q", nil, { exit = true, nowait = true } },
    },
})

